#include <stdio.h>
void	ft_swap(int	*a,	int *b)
{
	int temp = *a;
	*a = *b;
	*b = temp;
}

int main(void)
{
	int *a;
	int *b;
	int	d;
	int	c;
	c = 2;
	d = 4;
	printf("%d" "%d", c, d);
	ft_swap(&c, &d);
	printf("%d" "%d", c, d);
}
